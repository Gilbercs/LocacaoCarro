    /**
     * metodo carregar tabela
     */
    private void carregarListaCategoria(){
        ArrayList<ModelCategoria> listarCategoria = new ArrayList<>();
        listarCategoria = controlCategoria.controlGetListaCategoria();
        DefaultTableModel tableModel = (DefaultTableModel) tabelaCategoria.getModel();
        tableModel.setNumRows(0);
        int cont = listarCategoria.size();
        for(int i = 0; i < cont; i++){  
            tableModel.addRow(new Object[]{
                listarCategoria.get(i).getCatCodigo(),
                listarCategoria.get(i).getCatClasse(),
                listarCategoria.get(i).getCatPreco(),
                listarCategoria.get(i).getCatDescricao()
            });
        }
        
    }
    /**
     * metodo recuperar categoria na tabela
     */
    public void recuperarCategoria(){
        //recebe a linha selecionada
        int linha = this.tabelaCategoria.getSelectedRow();
        //pegar o codigo da categoria selecionado
        int catCodigo = (int) tabelaCategoria.getValueAt(linha, 0);
        try{
            //recuperar dados do banco de dados
            modelCategoria = controlCategoria.controlGetRecuperarCodigoCat(catCodigo);
            //seta dados na interface
            this.edtCatCodigo.setText(String.valueOf(modelCategoria.getCatCodigo()));
            this.edtCatClasse.setText(modelCategoria.getCatClasse());
            this.edtCatPreco.setText(String.valueOf(modelCategoria.getCatPreco()));
            this.edtCatDescricao.setText(modelCategoria.getCatDescricao());
           
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this,"Erro"+ex);
        }
        
    }
