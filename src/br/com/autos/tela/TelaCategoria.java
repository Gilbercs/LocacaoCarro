    private void btnCatPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCatPesquisarActionPerformed
        DefaultTableModel tabela = (DefaultTableModel) this.tabelaCategoria.getModel();
        final TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(tabela);
        this.tabelaCategoria.setRowSorter(sorter);
        String text = edtCarPesquisar.getText();
        sorter.setRowFilter(RowFilter.regexFilter(text, 1));
    }//GEN-LAST:event_btnCatPesquisarActionPerformed
/**
 * metodo para habilitar campos
 */
    public void habilitarCampos(){
    edtCatCodigo.setEnabled(false);
    edtCatClasse.setEnabled(true);
    edtCatPreco.setEnabled(true);
    edtCatDescricao.setEnabled(true);
}
    /**
     * metodo desabilitar campos
     */
    private void desabilitarCampos(){
    edtCatCodigo.setEnabled(false);
    edtCatClasse.setEnabled(false);
    edtCatPreco.setEnabled(false);
    edtCatDescricao.setEnabled(false);
    }
    /**
     * metodo habilitar botões
     */
    public void habilitarBotões(){
        btnCatNovo.setEnabled(false);
        btnCatSalvar.setEnabled(true);
        btnCatEditar.setEnabled(true);
        btnCatExcluir.setEnabled(true);
        btnCancelar.setEnabled(true);
    }
    /**
     * metodo desabilitar botao
     */
    private void desabilitarBotão(){
        btnCatSalvar.setEnabled(false);
        btnCatEditar.setEnabled(false);
        btnCatExcluir.setEnabled(false);
        btnCancelar.setEnabled(false);
        btnCatNovo.setEnabled(true);
    }
    /**
     * limpar campos
     */
    public void limparCampos(){
        edtCatCodigo.setText("");
        edtCatClasse.setText("");
        edtCatPreco.setText("");
        edtCatDescricao.setText("");
    }
