    public void excluirCategoria(){
        int linha = tabelaCategoria.getSelectedRow();
        String tCategoria = (String) tabelaCategoria.getValueAt(linha, 1);
        int codigo = (int) tabelaCategoria.getValueAt(linha, 0);
        int opcao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir a Categoria: \n"
                +tCategoria+"?","Atenção",JOptionPane.YES_NO_OPTION);
        if(opcao==JOptionPane.OK_OPTION){
            boolean resultado = controlCategoria.controlExcluirCategoria(codigo);
            if(resultado == true){
                JOptionPane.showMessageDialog(this, "categoria Excluida com sucesso!");
                carregarListaCategoria();
            }
        }
    }
    
