    public void excluirFuncionario(){
        int linha = tabelFuncionario.getSelectedRow();
        String tNome = (String) tabelFuncionario.getValueAt(linha, 1);
        int codigo = (int) tabelFuncionario.getValueAt(linha, 0);
        int opcao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir o Funcionario: \n"
                +tNome+"?","Atenção",JOptionPane.YES_NO_OPTION);
        if(opcao==JOptionPane.OK_OPTION){
            boolean resultado = controlFuncionario.controlGetDeleteFuncionario(codigo);
            if(resultado == true){
                JOptionPane.showMessageDialog(this, "Funcionário Excluido com sucesso!");
                carregarListaFuncionario();
            }
        }
    }
