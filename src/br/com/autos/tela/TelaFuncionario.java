    /**
     * carregar tabela
     */
    public void carregarListaFuncionario(){
        ArrayList<ModelFuncionario> listarFuncionario = new ArrayList<>();
        listarFuncionario = controlFuncionario.controlGetListaFuncionario();
        DefaultTableModel tableModel = (DefaultTableModel) tabelFuncionario.getModel();
        tableModel.setNumRows(0);
        int cont = listarFuncionario.size();
        for(int i = 0; i < cont; i++){  
            tableModel.addRow(new Object[]{
                listarFuncionario.get(i).getFunMatricula(),
                listarFuncionario.get(i).getFunNome(),
                listarFuncionario.get(i).getFunLogin()
            });
        }
        
    }
    /**
     * recuper Funcionario na tabela
     */
    public void recuperarFuncionario(){
        //recebe a linha selecionada
        int linha = this.tabelFuncionario.getSelectedRow();
        //pegar a matricula do funcionario selecionado
        int funMatricula = (int) tabelFuncionario.getValueAt(linha, 0);
        try{
            //recuperar dados do banco de dados
            modelFuncionario = controlFuncionario.controlGetRecuperarFunId(funMatricula);
            //seta dados na interface
            this.edtFunMatricula.setText(String.valueOf(modelFuncionario.getFunMatricula()));
            this.edtFunNome.setText(modelFuncionario.getFunNome());
            this.edtFunLogin.setText(modelFuncionario.getFunLogin());
            this.edtFunSenha.setText(modelFuncionario.getSenha());
            this.edtFunConfirmaSenha.setText(modelFuncionario.getSenha());
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this,"Erro");
        }
        
    }
