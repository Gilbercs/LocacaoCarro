     /**
     * metodo limpar campos
     */
    public void limparCampos(){
        edtFunMatricula.setText("");
        edtFunNome.setText("");
        edtFunLogin.setText("");
        edtFunSenha.setText("");
        edtFunConfirmaSenha.setText("");
    }
    /**
     * metodo para desabilitar campos
     */
    public void desabilitarCampos(){
        edtFunMatricula.setEnabled(false);
        edtFunNome.setEnabled(false);
        edtFunLogin.setEnabled(false);
        edtFunSenha.setEnabled(false);
        edtFunConfirmaSenha.setEnabled(false);
    }
    /**
     * Habilitar campos para entrar com os dados do funcionario
     */
    public void habilitarCampos(){
        edtFunMatricula.setText("Novo");
        edtFunMatricula.setEnabled(false);
        edtFunNome.setEnabled(true);
        edtFunLogin.setEnabled(true);
        edtFunSenha.setEnabled(true);
        edtFunConfirmaSenha.setEnabled(true);
        
    }
    /**
     * metodo para desabilitar botão
     */
    public void desabilitarBotao(){
        btnFunCancelar.setEnabled(false);
        btnFunSalvarALteraçao.setEnabled(false);
        btnFunSalvar.setEnabled(false);
        btnFunExcluir.setEnabled(false);
        btnFunAlterar.setEnabled(false);
    }
    /**
     * metodo habilitar botões
     */
    public void habilitarBotao(){
        btnFunCancelar.setEnabled(true);
        btnFunSalvarALteraçao.setEnabled(true);
        btnFunSalvar.setEnabled(true);
        btnFunExcluir.setEnabled(true);
        btnFunAlterar.setEnabled(true);
    }
